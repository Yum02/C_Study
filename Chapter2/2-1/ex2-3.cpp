#include <stdio.h>

int main()
{
	printf("Hi\n");
	printf("Nice to meet you\n"); // \n is a newline character

	printf("My name is\t Yum!\n"); // \t is a tab character

	printf("Goot\bd\n"); // \b is a backspace character

	printf("Cow\rW\n"); // \r is a carriage return character

	printf("!\a\n"); // \a is a bell character

	return 0;

}